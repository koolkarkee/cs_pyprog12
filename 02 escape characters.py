#print("My Drive is C:\")
#print("My name is \bibek")

#\n new line 
print("\nI live in Kathmandu\nI also live in Sindhupalchok.")

#\\ backslash 
print("\\I like to travel.")
print("C:\\Users\\Admin\\Desktop")

#\' single quotes
print("\'And I quote .. '")
print("\'William Shakespeare'")

#\" double quotes
print('\"And I quote .."')
print('\"hello"')

#\a bell (ASCII Bell (BEL))
print('hello\aMr. Bibek')

#\b regression (ASCII backspace)
print("I'm\bMr. Bibek")

#\t horizontal tab
print("Name:\tBibek Karki\tRoll No:\t24")

print("\n")

#\v vertical tab 
print("Name\vBibek Karki")
print("Name\tAge\tRoll\n\vBibek\t20\t101")

#\r carriage return 
#print("hello1\rworld2\rMr.Bibek3\rMr.Ram4") 

#\f form feed
print("Ram\fKumar\fKarki")
print("1\f1\f1\f1\f1")

#\0oXX octal value
octal_num = 0o101
print(octal_num) #prints decimal equivalent 

#\0xXX hexadecimal value 
hex_num = 0x41
print(hex_num) #prints decimal equivalent

#printing binary number
binary_num = 0b101
